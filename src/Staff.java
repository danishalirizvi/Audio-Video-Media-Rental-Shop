public class Staff {
	
	int id;
	String name;
	int PHNE;
	String username;
	String password;
	
	public Staff(int id, String name, int pHNE, String username, String password) {
		super();
		this.id = id;
		this.name = name;
		PHNE = pHNE;
		this.username = username;
		this.password = password;
	}
	
}
